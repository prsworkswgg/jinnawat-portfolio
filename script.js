const supportedLanguages = ["en", "th"];
const savedLanguage = localStorage.getItem("portfolioLanguage");
let currentLanguage = supportedLanguages.includes(savedLanguage) ? savedLanguage : "en";

const body = document.body;
const navToggle = document.querySelector("[data-nav-toggle]");
const nav = document.querySelector("[data-nav]");
const langButtons = Array.from(document.querySelectorAll("[data-lang-button]"));

function getText(key, language = currentLanguage) {
  return translations[language]?.[key] ?? translations.en[key] ?? "";
}

function updateNavToggleLabel(isOpen = body.classList.contains("nav-open")) {
  const key = isOpen ? navToggle?.dataset.i18nCloseLabel : navToggle?.dataset.i18nOpenLabel;
  if (key) {
    navToggle.setAttribute("aria-label", getText(key));
  }
}

function closeNav() {
  body.classList.remove("nav-open");
  navToggle?.setAttribute("aria-expanded", "false");
  updateNavToggleLabel(false);
}

function applyLanguage(language, shouldPersist = true) {
  currentLanguage = supportedLanguages.includes(language) ? language : "en";

  if (shouldPersist) {
    localStorage.setItem("portfolioLanguage", currentLanguage);
  }

  document.documentElement.lang = currentLanguage;
  document.title = getText("page.title");

  const description = document.querySelector('meta[name="description"]');
  if (description) {
    description.setAttribute("content", getText("page.description"));
  }

  document.querySelectorAll("[data-i18n]").forEach((element) => {
    element.textContent = getText(element.dataset.i18n);
  });

  document.querySelectorAll("[data-i18n-html]").forEach((element) => {
    element.innerHTML = getText(element.dataset.i18nHtml);
  });

  document.querySelectorAll("[data-i18n-aria]").forEach((element) => {
    element.setAttribute("aria-label", getText(element.dataset.i18nAria));
  });

  langButtons.forEach((button) => {
    const active = button.dataset.langButton === currentLanguage;
    button.classList.toggle("active", active);
    button.setAttribute("aria-pressed", String(active));
  });

  updateNavToggleLabel();
}

navToggle?.addEventListener("click", () => {
  const isOpen = body.classList.toggle("nav-open");
  navToggle.setAttribute("aria-expanded", String(isOpen));
  updateNavToggleLabel(isOpen);
});

nav?.addEventListener("click", (event) => {
  if (event.target.closest("a")) {
    closeNav();
  }
});

langButtons.forEach((button) => {
  button.addEventListener("click", () => {
    applyLanguage(button.dataset.langButton);
  });
});

document.addEventListener("keydown", (event) => {
  if (event.key === "Escape") {
    closeNav();
  }
});

applyLanguage(currentLanguage, false);
